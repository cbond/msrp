#include "msrp/MessageSessionBase.hxx"

using namespace msrp;
using namespace std;
using namespace boost;

MessageSessionBase::MessageSessionBase(const Message& m) :
   mMessage(m), mSize(0), mTransferred(0), mComplete(false), mInterrupted(false)
{
   try
   {
      if (mMessage.exists<ByteRange>())
      {
         const ByteRangeTuple& br = mMessage.header<ByteRange>();

         if (br.total != ByteRange::Unknown)
         {
            mSize = br.total;
         }
      }
   }
   catch (const ParseException&)
   {}
}

MessageSessionBase::~MessageSessionBase()
{}

size_t
MessageSessionBase::size() const
{
   ScopedLock lock(mMutex);

   return mSize;
}

size_t
MessageSessionBase::transferred() const
{
   ScopedLock lock(mMutex);

   return mTransferred;
}

bool
MessageSessionBase::complete() const
{
   ScopedLock lock(mMutex);

   return mComplete;
}

bool
MessageSessionBase::interrupted() const
{
   ScopedLock lock(mMutex);

   return mInterrupted;
}

Message&
MessageSessionBase::message()
{
   ScopedLock lock(mMutex);

   return mMessage;
}

const Message&
MessageSessionBase::message() const
{
   ScopedLock lock(mMutex);

   return mMessage;
}

const string
MessageSessionBase::messageId() const
{
   ScopedLock lock(mMutex);

   try
   {
      return message().header<MessageId>();
   }
   catch (const ParseException&)
   {}

   return std::string();
}

signal0<void>&
MessageSessionBase::onComplete()
{
   ScopedLock lock(mMutex);

   return mCompleteEvent;
}

const posix_time::ptime&
MessageSessionBase::lastTransfer() const
{
   ScopedLock lock(mMutex);

   return mLastTransfer;
}

